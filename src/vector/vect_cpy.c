#include "raytracer.h"

void	vect_cpy(t_vect *dst, t_vect *src)
{
	(*dst)[X] = (*src)[X];
	(*dst)[Y] = (*src)[Y];
	(*dst)[Z] = (*src)[Z];
	(*dst)[W] = (*src)[W];
}
